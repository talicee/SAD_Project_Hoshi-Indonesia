CREATE TABLE MASTER_ITEM (
    ID_ITEM VARCHAR(10) PRIMARY KEY,
    NAME_ITEM VARCHAR(50) NOT NULL,
    COLOUR_ITEM VARCHAR(10) NOT NULL,
    SIZE_ITEM VARCHAR(18) NOT NULL,
    CATEGORY_ITEM VARCHAR(9) NOT NULL,
    SALES_PRICE_ITEM INT UNSIGNED NOT NULL,
    PURCHASE_PRICE_ITEM INT UNSIGNED NOT NULL,
    STOCK_ITEM INT UNSIGNED NOT NULL DEFAULT 0,
    MINIMUM_STOCK_ITEM INT UNSIGNED NOT NULL DEFAULT 10,
    STATUS_DEL_ITEM BOOLEAN NOT NULL DEFAULT 0
);

CREATE TABLE MASTER_SUPPLIER (
    ID_SUPPLIER VARCHAR(4) PRIMARY KEY,
    COMPANY_SUPPLIER VARCHAR(50) NOT NULL,
    ADDRESS_SUPPLIER VARCHAR(100) NOT NULL,
    CITY_SUPPLIER VARCHAR(20) NOT NULL,
    PHONE_NUMBER_SUPPLIER VARCHAR(13) NOT NULL,
    EMAIL_SUPPLIER VARCHAR(50) NOT NULL,
    CONTACT_PERSON_SUPPLIER VARCHAR(50) NOT NULL,
    TOP_SUPPLIER VARCHAR(7) NOT NULL,
    STATUS_DEL_SUPPLIER BOOLEAN NOT NULL DEFAULT 0
);

CREATE TABLE MASTER_CUSTOMER (
    ID_CUSTOMER VARCHAR(5) PRIMARY KEY,
    INSTANCE_CUSTOMER VARCHAR(50) NOT NULL,
    ADDRESS_CUSTOMER VARCHAR(100) NOT NULL,
    CITY_CUSTOMER VARCHAR(20) NOT NULL,
    PHONE_NUMBER_CUSTOMER VARCHAR(15) NOT NULL,
    EMAIL_CUSTOMER VARCHAR(50) NOT NULL,
    CONTACT_PERSON_CUSTOMER VARCHAR(50) NOT NULL,
    REGISTERED_DATE_CUSTOMER TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    TOP_CUSTOMER VARCHAR(7) NOT NULL DEFAULT 'Cash',
    STATUS_DEL_CUSTOMER BOOLEAN NOT NULL DEFAULT 0
);

CREATE TABLE SALES_TRANSACTION (
    ID_SALES VARCHAR(11) PRIMARY KEY,
    TRANSACTION_DATE_SALES TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ID_CUSTOMER VARCHAR(5) NOT NULL,
    NAME_CUSTOMER VARCHAR(50) NOT NULL,
    ADDRESS_CUSTOMER VARCHAR(100) NOT NULL,
    CITY_CUSTOMER VARCHAR(20) NOT NULL,
    TOP_CUSTOMER VARCHAR(7) NOT NULL,
    DUE_DATE_PAYMENT_SALES TIMESTAMP NOT NULL,
    PAYMENT_STATUS_SALES VARCHAR(6) NOT NULL,
    GRAND_TOTAL_SALES INT UNSIGNED NOT NULL,
    STATUS_DEL_SALES BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_CUSTOMER) REFERENCES MASTER_CUSTOMER(ID_CUSTOMER)
);

CREATE TABLE DETAIL_SALES_TRANSACTION (
	ID_DETAIL_SALES INT AUTO_INCREMENT PRIMARY KEY,
    ID_SALES VARCHAR(11) NOT NULL,
    ID_ITEM VARCHAR(10) NOT NULL,
    NAME_ITEM VARCHAR(50) NOT NULL,
    SALES_PRICE_ITEM INT UNSIGNED NOT NULL,
    QUANTITY_DETAILSALES INT UNSIGNED NOT NULL,
    TOTAL_PRICE_DETAILSALES INT UNSIGNED NOT NULL,
    STATUS_DEL_DETAILSALES BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_SALES) REFERENCES SALES_TRANSACTION(ID_SALES),
    FOREIGN KEY (ID_ITEM) REFERENCES MASTER_ITEM(ID_ITEM)
);

CREATE TABLE PURCHASE_TRANSACTION (
    ID_PURCHASE VARCHAR(11) PRIMARY KEY,
    TRANSACTION_DATE_PURCHASE TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ID_SUPPLIER VARCHAR(4) NOT NULL,
    NAME_SUPPLIER VARCHAR(50) NOT NULL,
    ADDRESS_SUPPLIER VARCHAR(100) NOT NULL,
    CITY_SUPPLIER VARCHAR(20) NOT NULL,
    INVOICE_SUPPLIER_PURCHASE VARCHAR(30) NOT NULL,
    TOP_SUPPLIER VARCHAR(7) NOT NULL,
    DUE_DATE_PAYMENT_PURCHASE TIMESTAMP NOT NULL,
    PAYMENT_STATUS_PURCHASE VARCHAR(6) NOT NULL,
    GRAND_TOTAL_PURCHASE INT UNSIGNED NOT NULL,
    STATUS_DEL_PURCHASE BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_SUPPLIER) REFERENCES MASTER_SUPPLIER(ID_SUPPLIER)
);

CREATE TABLE DETAIL_PURCHASE_TRANSACTION (
    ID_DETAIL_PURCHASE INT AUTO_INCREMENT PRIMARY KEY,
    ID_PURCHASE VARCHAR(11) NOT NULL,
    ID_ITEM VARCHAR(10) NOT NULL,
    NAME_ITEM VARCHAR(50) NOT NULL,
    PURCHASE_PRICE_ITEM INT UNSIGNED NOT NULL,
    QUANTITY_DETAILPURCHASE INT UNSIGNED NOT NULL,
    TOTAL_PRICE_DETAILPURCHASE INT UNSIGNED NOT NULL,
    STATUS_DEL_DETAILPURCHASE BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_PURCHASE) REFERENCES PURCHASE_TRANSACTION(ID_PURCHASE),
    FOREIGN KEY (ID_ITEM) REFERENCES MASTER_ITEM(ID_ITEM)
);

CREATE TABLE PURCHASE_PRICE_HISTORY (
    ID_PURCHASEPRICEHISTORY INT AUTO_INCREMENT PRIMARY KEY,
    DATE_PURCHASEPRICEHISTORY TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ID_ITEM VARCHAR(10) NOT NULL,
    PREVIOUS_PURCHASE_PRICE_ITEM INT UNSIGNED NOT NULL,
    NEW_PURCHASE_PRICE_PURCHASEPRICEHISTORY INT UNSIGNED NOT NULL,
    STATUS_DEL_PURCHASEPRICEHISTORY BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_ITEM) REFERENCES MASTER_ITEM(ID_ITEM)
);

CREATE TABLE SALES_PRICE_HISTORY (
    ID_SALESPRICEHISTORY INT AUTO_INCREMENT PRIMARY KEY,
    DATE_SALESPRICEHISTORY TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ID_ITEM VARCHAR(10) NOT NULL,
    PREVIOUS_SALES_PRICE_ITEM INT UNSIGNED NOT NULL,
    NEW_PURCHASE_PRICE_SALESPRICEHISTORY INT UNSIGNED NOT NULL,
    STATUS_DEL_SALESPRICEHISTORY BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_ITEM) REFERENCES MASTER_ITEM(ID_ITEM)
);

CREATE TABLE STOCK_OPNAME (
    ID_STOCKOPNAME INT AUTO_INCREMENT PRIMARY KEY,
    DATE_STOCKOPNAME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP, 
    ID_ITEM VARCHAR(10) NOT NULL, 
    SYSTEM_STOCK_ITEM INT UNSIGNED NOT NULL, 
    PHYSICAL_STOCK_STOCKOPNAME INT UNSIGNED NOT NULL,
    INFORMATION_STOCKOPNAME VARCHAR(100), 
    STATUS_DEL_STOCKOPNAME BOOLEAN NOT NULL DEFAULT 0,
    FOREIGN KEY (ID_ITEM) REFERENCES MASTER_ITEM(ID_ITEM) 
);


